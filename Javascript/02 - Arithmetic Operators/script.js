// Arithmetic operators in javascript are fundamental for perfoming mathematical calculations
// in your programs. They allow you to work with numerical data types, such as integers
// and floating-point numbers, to perform addition, subtraction, multiplication, division and more.

// არითმეტიკული ოპერატორები ჯავასკრიპტში არის ფუნდამენტური ნაწილი არითმეტიკული
// ოპერაციების შესასრულებლად. ისინი იძლევიან საშვალებას იმუშაო რიცხვებთან, როგორიცაა
// ინტეჯერები (რიცხვები) და ტელი რიცხვები, რომ შეასრულო დამატება, გამოკლება, გამრავლება, გაყოფა და სხვა.

////////////////////////////////////////////////////////////////////////////

// 1. Addition ('+')
// The addition operator is used to add two values together.
// 1. დამატება ('+')
// დამატების ოპერატორი ამატებს ორ მნიშვნელობას ერთად
let sum = 5 + 7;
console.log(sum);
// The sum is 12
// ჯამი არის 12

////////////////////////////////////////////////////////////////////////////

// 2. Subtraction ('-')
// The subtraction operator is used to subtract one value from another.
// 2. გამოკლება ('-')
// გამოკლების ოპერატორი ერთ მნიშნელობას აკლებს მეორეს.
let difference = 10 - 4;
console.log(difference);
// The difference is 6
// სხვაობა არის 6

////////////////////////////////////////////////////////////////////////////

// 3. Multiplication ('*')
// The multiplication operator multiplies two values together.
// 3. გამრავლება ('*')
// გამრავლების ოპერატორი ერთ მნიშვნელობას ამრავლებს მეორეზე
let product = 3 * 5;
console.log(product);
// The product is 15.
// ნამრავლი არის 15

////////////////////////////////////////////////////////////////////////////

// 4. Division ('/')
// The division operator is used to divide one value by another.
// 4. გაყოფა ('/')
// გაყოფის ოპერატორი ყოფს ერთ მნიშვნელობას მეორეზე
let quotient = 10 / 2;
console.log(quotient);
// The quotient is 5.
// განაყოფი არის 5

////////////////////////////////////////////////////////////////////////////

// 5. Modulus ('%')
// The modulus operator returns the remainder of a division operation.
// 5. მოდულუსი ('%')
// მოდულუსი აბრუნებს განაყოფის ნაშთს
let remainder = 10 % 3;
console.log(remainder);
// The remainder is 1. (10 / 3 = 9 (+1))
// ნაშთი არის 1 (10 / 3 = 9 (+1))

////////////////////////////////////////////////////////////////////////////

// 6. Exponentiation ('**') - ES6
// The exponentiation operator raises the left operabd to the power ofright operand.
let result = 2 ** 3;
console.log(result);
// The result is 8 (2 raised to the power of 3)