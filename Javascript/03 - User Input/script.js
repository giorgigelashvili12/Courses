// prompt() - This function displays a dialog box that prompts the user for input.
// It takes an optional message as an argument and returns the user's value as a string.
// prompt() - ამ ფუნქციას გამოაქვს ტექსტი ყუთში. ის იღებს შეტანილ მესიჯს არგუმენტად 
// სტრინგის ნახით.

let userName = prompt("Enter Your Username: ");
console.log("Hello, " + userName + "!");
// Here, 'prompt("Enter Your Username: ")' displays a dialog with the message: 
// "Enter Your Username:". Value entered by the user is stored in the variable 'userName'

