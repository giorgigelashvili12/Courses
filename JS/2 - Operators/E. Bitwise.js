// Bit operators work on 32 bits numbers

// AND - &
// compares each bit of two numbers and returns a new number where bits
// are set to 1 only if both bits are 1
5 & 1 // same as 010 & 0001 == 0001

// OR - |
5 | 1 // 0101 | 0001 == 0101

// NOT - ~
~ 5 // ~0101 == 1010

// XOR - ^
5 ^ 1 // 0101 ^ 0001 == 0100