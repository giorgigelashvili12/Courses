// In JavaScript, functions are blocks of code that can be defined and 
// called to perform a specific task or calculate a value. They can 
// accept parameters and return values.
function add(a, b) {
    return a + b;
}
  
let result = add(3, 4);
console.log(result); // Output: 7

// function parameters and return function
function greet(name) {
    return `Hello, ${name}!`;
}
  
let greeting = greet("John");
console.log(greeting); // Output: Hello, John!
  